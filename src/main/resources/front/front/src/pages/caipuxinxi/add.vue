    <el-form
      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="80px"
    >
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="菜谱名称" prop="caipumingcheng">
            <el-input v-model="ruleForm.caipumingcheng" 
                placeholder="菜谱名称" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="菜谱封面" v-if="type!='cross' || (type=='cross' && !ro.caipufengmian)" prop="caipufengmian">
            <file-upload
            tip="点击上传菜谱封面"
            action="file/upload"
            :limit="3"
            :multiple="true"
            :fileUrls="ruleForm.caipufengmian?ruleForm.caipufengmian:''"
            @change="caipufengmianUploadChange"
            ></file-upload>
          </el-form-item>
            <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' class="upload" v-else label="菜谱封面" prop="caipufengmian">
                <img v-if="ruleForm.caipufengmian.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.caipufengmian.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.caipufengmian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}'  label="菜式类型" prop="caishileixing">
            <el-select v-model="ruleForm.caishileixing" placeholder="请选择菜式类型"  >
              <el-option
                  v-for="(item,index) in caishileixingOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="烹饪方式" prop="pengrenfangshi">
            <el-input v-model="ruleForm.pengrenfangshi" 
                placeholder="烹饪方式" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="分数" prop="fenshu">
            <el-input v-model="ruleForm.fenshu" 
                placeholder="分数" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="发布日期" prop="faburiqi" >
              <el-date-picker
                  format="yyyy 年 MM 月 dd 日"
                  value-format="yyyy-MM-dd"
                  v-model="ruleForm.faburiqi" 
                  type="date"
                  placeholder="发布日期">
              </el-date-picker> 
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="材料" prop="cailiao">
            <el-input
              type="textarea"
              :rows="8"
              placeholder="材料"
              v-model="ruleForm.cailiao">
            </el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"0"}' label="制作流程" prop="zhizuoliucheng">
            <editor 
                :style='{"border":"1px solid #cbbae8","width":"100%","borderRadius":"4px","height":"auto"}'
                v-model="ruleForm.zhizuoliucheng" 
                class="editor" 
                action="file/upload">
            </editor>
          </el-form-item>

