<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            caipumingcheng : false,
            caipufengmian : false,
            caishileixing : false,
            pengrenfangshi : false,
            fenshu : false,
            cailiao : false,
            zhizuoliucheng : false,
            faburiqi : false,
            clicktime : false,
            clicknum : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          caipumingcheng: '',
          caipufengmian: '',
          caishileixing: '',
          pengrenfangshi: '',
          fenshu: '0',
          cailiao: '',
          zhizuoliucheng: '',
          faburiqi: '',
          clicktime: '',
          clicknum: '',
        },
        caishileixingOptions: [],
        rules: {
          caipumingcheng: [
            { required: true, message: '菜谱名称不能为空', trigger: 'blur' },
          ],
          caipufengmian: [
          ],
          caishileixing: [
            { required: true, message: '菜式类型不能为空', trigger: 'blur' },
          ],
          pengrenfangshi: [
          ],
          fenshu: [
            { required: true, message: '分数不能为空', trigger: 'blur' },
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          cailiao: [
          ],
          zhizuoliucheng: [
          ],
          faburiqi: [
          ],
          clicktime: [
          ],
          clicknum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
        },
      };
    },
    computed: {



    },
    components: {
    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.faburiqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='caipumingcheng'){
              this.ruleForm.caipumingcheng = obj[o];
              this.ro.caipumingcheng = true;
              continue;
            }
            if(o=='caipufengmian'){
              this.ruleForm.caipufengmian = obj[o].split(",")[0];
              this.ro.caipufengmian = true;
              continue;
            }
            if(o=='caishileixing'){
              this.ruleForm.caishileixing = obj[o];
              this.ro.caishileixing = true;
              continue;
            }
            if(o=='pengrenfangshi'){
              this.ruleForm.pengrenfangshi = obj[o];
              this.ro.pengrenfangshi = true;
              continue;
            }
            if(o=='fenshu'){
              this.ruleForm.fenshu = obj[o];
              this.ro.fenshu = true;
              continue;
            }
            if(o=='cailiao'){
              this.ruleForm.cailiao = obj[o];
              this.ro.cailiao = true;
              continue;
            }
            if(o=='zhizuoliucheng'){
              this.ruleForm.zhizuoliucheng = obj[o];
              this.ro.zhizuoliucheng = true;
              continue;
            }
            if(o=='faburiqi'){
              this.ruleForm.faburiqi = obj[o];
              this.ro.faburiqi = true;
              continue;
            }
            if(o=='clicktime'){
              this.ruleForm.clicktime = obj[o];
              this.ro.clicktime = true;
              continue;
            }
            if(o=='clicknum'){
              this.ruleForm.clicknum = obj[o];
              this.ro.clicknum = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/caishileixing/caishileixing', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.caishileixingOptions = res.data.data;
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('caipuxinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('caipuxinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('caipuxinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('caipuxinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      caipufengmianUploadChange(fileUrls) {
          this.ruleForm.caipufengmian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

